public class Main {
    public static void main(String[] args){
        byte age = 18;
        int bigNumber = 1000;
        System.out.println("byte data type");
        System.out.println(age);
        System.out.println("int data type");
        System.out.println(bigNumber);
        System.out.println("double data type");
        double decimal = 15.5;
        System.out.println(decimal);
        System.out.println("float data type");
        float floatNumber = 15.52F;
        System.out.println(floatNumber);
        boolean isOlder = true;
        char course = 'C';
        System.out.println(isOlder);
        System.out.println(course);

    }
}
